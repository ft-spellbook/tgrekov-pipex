#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t	i;
	int		diff;

	i = 0;
	while (i < n)
	{
		diff = ((unsigned char *) s1)[i] - ((unsigned char *) s2)[i];
		if (diff)
			return (diff);
		i++;
	}
	return (0);
}
